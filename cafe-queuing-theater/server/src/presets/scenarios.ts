import { PresetScenario, MapElement, Seat, Employee, Point, DEFAULT_CONFIG } from '../types';

const createBar = (id: string, x: number, y: number, width: number, height: number): MapElement => ({
  id,
  type: 'bar',
  polygon: [
    { x, y },
    { x: x + width, y },
    { x: x + width, y: y + height },
    { x, y: y + height },
  ],
  properties: { name: `吧台-${id}`, width, height },
});

const createSeatElement = (id: string, x: number, y: number, width: number, height: number = width): MapElement => ({
  id,
  type: 'seat',
  polygon: [
    { x, y },
    { x: x + width, y },
    { x: x + width, y: y + height },
    { x, y: y + height },
  ],
  properties: { width, height },
});

const createEntrance = (id: string, x: number, y: number, width: number, height: number): MapElement => ({
  id,
  type: 'entrance',
  polygon: [
    { x, y },
    { x: x + width, y },
    { x: x + width, y: y + height },
    { x, y: y + height },
  ],
  properties: { name: `入口-${id}`, direction: 'in' },
});

const createObstacle = (id: string, x: number, y: number, width: number, height: number): MapElement => ({
  id,
  type: 'obstacle',
  polygon: [
    { x, y },
    { x: x + width, y },
    { x: x + width, y: y + height },
    { x, y: y + height },
  ],
  properties: { name: `障碍物-${id}` },
});

const createSeat = (id: string, x: number, y: number, capacity: number): Seat => ({
  id,
  position: { x, y },
  capacity,
  occupied: 0,
  reserved: false,
});

const createEmployee = (stationX: number, stationY: number, skillMatrix: number[]): Omit<Employee, 'id'> => ({
  position: { x: stationX, y: stationY },
  station: { x: stationX, y: stationY },
  skillMatrix,
  fatigue: 0,
  efficiency: 1.0,
  processingTime: 0,
});

